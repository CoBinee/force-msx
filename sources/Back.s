; 背景を更新する
;
_BackUpdate::
    
    ; レジスタの保存

    ; 地面のスクロール
    ld      a, (_back + BACK_SCROLL_GROUND)
    and     #0x01
    add     a, a
    ld      c, a
    add     a, a
    ld      d, a
    ld      e, #0x00
    ld      hl, #backPatternNameGround
    add     hl, de
    ld      a, (_back + BACK_SCROLL_GROUND)
    add     a, c
    and     #0x1e
    ld      d, e
    add     a, a
    rl      d
    add     a, a
    rl      d
    add     a, a
    rl      d
    add     a, a
    rl      d
    ld      e, a
    add     hl, de
    ld      de, #_appPatternName
    ld      bc, #0x0200
    ldir
    ld      hl, #_appPatternName
    ld      bc, #0x0100
    ldir
    ld      hl, #(_back + BACK_SCROLL_GROUND)
    dec     (hl)

    ; 壁のスクロール
    ld      a, (_back + BACK_SCROLL_WALL)
    and     #0x07
    add     a, a
    add     a, a
    add     a, a
    ld      e, a
    ld      d, #0x00
    ld      hl, #backPatternNameWall
    add     hl, de
    ld      de, #_appPatternName
    ld      c, #0x03
20$:
    ld      b, #0x08
    push    hl
21$:
    push    bc
    ld      bc, #0x0004
    ldir
    ld      bc, #(0x0020 - 0x0008)
    ex      de, hl
    add     hl, bc
    ex      de, hl
    ld      bc, #0x0004
    ldir
    pop     bc
    djnz    21$
    pop     hl
    dec     c
    jr      nz, 20$
    ld      hl, #(_back + BACK_SCROLL_WALL)
    dec     (hl)

    ; フェードイン
30$:
    ld      hl, #(_game + GAME_FLAG)
    bit     #GAME_FLAG_OVER_BIT, (hl)
    jr      nz, 40$
    ld      hl, #(_back + BACK_FADE)
    ld      a, (hl)
    or      a
    jr      z, 39$
    dec     (hl)
    ld      c, (hl)
    ld      hl, #(_appPatternName + 0x02ff)
    ld      b, #0x00
31$:
    ld      a, c
    sub     b
    jr      c, 33$
    cp      #0x04
    jr      c, 32$
    ld      a, #0x03
32$:
    add     a, #0x8c
    push    bc
    ld      e, l
    ld      d, h
    dec     hl
    ex      de, hl
    ld      bc, #(0x0020 - 0x0001)
    ld      (hl), a
    lddr
    dec     hl
    pop     bc
    jr      34$
33$:
    ld      de, #-0x0020
    add     hl, de
34$:
    inc     b
    ld      a, b
    cp      #0x18
    jr      c, 31$
39$:
    jr      90$

    ; フェードアウト
40$:
    ld      hl, #(_back + BACK_FADE)
    ld      c, (hl)
    ld      hl, #_appPatternName
    ld      b, #0x00
41$:
    ld      a, b
    cp      c
    jr      nc, 43$
    ld      a, c
    sub     b
    cp      #0x03
    jr      c, 42$
    ld      a, #0x03
42$:
    add     a, #0x8c
    push    bc
    ld      e, l
    ld      d, h
    inc     hl
    ex      de, hl
    ld      bc, #(0x0020 - 0x0001)
    ld      (hl), a
    ldir
    inc     hl
    pop     bc
    jr      44$
43$:
    ld      de, #0x0020
    add     hl, de
44$:
    inc     b
    ld      a, b
    cp      #0x18
    jr      c, 41$
    ld      hl, #(_back + BACK_FADE)
    ld      a, (hl)
    cp      #(0x18 + 0x04)
    jr      nc, 49$
    inc     (hl)
49$:
;   jr      90$

    ; 背景の完了
90$:

    ; レジスタの復帰
    
    ; 終了
    ret

