; 背景がフェード中かどうかを確認する
;
_BackIsFade::

    ; レジスタの保存

    ; cf > フェード中

    ; フェードの確認
    ld      a, (_back + BACK_FADE)
    or      a
    jr      z, 10$
    scf
10$:

    ; レジスタの復帰

    ; 終了
    ret

